public class ArithemeticOperations{
	public static void main(String []args){
		
		/* Starting ArithemeticOperations*/
		double a = 1.000000;
		double b = 2.000000; 
		
		System.out.println("Value Of A : "+ a +"\n");
		System.out.println("Value Of B : "+ b +"\n\n");		
		System.out.println("Addition of A & B: "+ (a+b) +"\n");
		System.out.println("Substraction of A & B: "+ (a-b) +"\n");
		System.out.println("Multiplication of A & B: "+ (a*b) +"\n");
		System.out.println("Division of A & B: "+ (a/b) +"\n");
		System.out.println("Modulas of A & B: "+ (a%b) +"\n");
		System.out.println("Increment of A : "+ (++a) +"\n");
		System.out.println("Decrement of A : "+ (--a) +"\n");		
		
	}
}